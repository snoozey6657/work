// server.js - Main Express entry point
require('dotenv').config();
const express = require('express');
const cors    = require('cors');
const path    = require('path');

const projectsRouter = require('./routes/projects');

const app  = express();
const PORT = process.env.PORT || 4000;

// â”€â”€ Middleware â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
app.use(cors());                        // Allow requests from React frontend / scraper
app.use(express.json({ limit: '10mb' })); // Parse JSON bodies (for /api/import)

// â”€â”€ API Routes â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
app.use('/api/projects', projectsRouter);

// Health check
app.get('/api/health', (req, res) => res.json({ status: 'ok' }));

// â”€â”€ Serve React frontend in production â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
// In development the CRA dev server handles the frontend.
// In production (Railway), we serve the built static files.
const buildPath = path.join(__dirname, '../frontend/build');
app.use(express.static(buildPath));

// All non-API routes fall through to the React app (client-side routing)
app.get('*', (req, res) => {
  res.sendFile(path.join(buildPath, 'index.html'));
});

// â”€â”€ Start â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
app.listen(PORT, () => {
  console.log(`ðŸš€  API server running at http://localhost:${PORT}`);
});
